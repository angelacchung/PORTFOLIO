            <p>
                For phase 2, I analyzed for UX and UI issues on both Android and iOs devices. I found that:
            </p>
            <ul>
                <li>
                    Blog posts may be irrelevant to users’ needs and interests. As a result, it’s hard for the user to
                    identify the content they would want to read.
                </li>
                <li>
                    There is no clear indication that the user is being onboarded to chatting with a doctor.
                </li>
                <li>
                    Medication: If a user puts in a medication that is not from the drop down list (ex: Advil),
                    it looks like it has been added to the list but the button turns into a loading animation
                    and just keeps looping.
                </li>
                <li>
                    ‘Connecting you with a doctor’ popup can be exited but it takes you out of the queue which is
                    unexpected.
                </li>
                <li>
                    Lack of giving the user a sense of progress being made
                </li>
            </ul>

