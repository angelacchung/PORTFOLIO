                <p>
                    My next step was to watch some recordings from UX Cam and see how users behave.
                    A few observations and UX issues that I found were:

                </p>
                <ul>
                    <li>
                        In the chatroom, users were tapping on the doctor’s profile picture.
                    </li>
                    <li>
                        Doctors often have to ask their users to elaborate on their medical issue
                    </li>
                    <li>
                        Adding a dosage for medicine is confusing. Here, UX Cam shows that the user is rage tapping the
                        screen.
                    </li>
                </ul>
                <p>
                    I also observed that none of the UX Cam recordings showed users that were interested in
                    the articles. Some users also would just leave a chatroom in the middle of a conversation
                    with a doctor, possibly due to impatience.

                </p>

